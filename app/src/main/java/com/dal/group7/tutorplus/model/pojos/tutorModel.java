package com.dal.group7.tutorplus.model.pojos;

public class tutorModel {


	String name;
	String age;
	String Phone;
	String Category;
	String Level;
	String Qualification;
	String PriceperSession;
	String TutorExperience;


	public String getName() {
		return name;
	}
	public void setName(String name) {
		name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getPhone() {
		return Phone;
	}
	public void setPhone(String phone) {
		this.Phone = Phone;
	}

	public String getCategory() {
		return Category;
	}
	public void setCategory(String Category) {
		this.Category = Category;
	}

	public String getLevel() {
		return Level;
	}
	public void setLevel(String Level) {
		this.Level = Level;
	}


	public String getQualification() {
		return Qualification;
	}
	public void setQualification(String Qualification) {
		this.Qualification = Qualification;
	}

	public String getPriceperSession() {
		return PriceperSession;
	}
	public void setPriceperSession(String PriceperSession) {
		this.PriceperSession = PriceperSession;
	}

	public String getTutorExperience() {
		return TutorExperience;
	}
	public void setTutorExperience(String TutorExperience) {
		this.TutorExperience = TutorExperience;
	}

}


